	[EXPIRY_DATE]
    ,[DATE_CREATED]
    ,[USER_CREATED]
    ,[USER_UPDATED]
    ,[DATE_UPDATED]
    ,[EMPLOYEE_ID]
    ,[HR_REQUEST_ID]
    ,[EMPLOYEE_ROLE_ID])
VALUES (
	NULL
    ,GETDATE()
    ,@pUSER_UPDATED
    ,NULL
    ,NULL
    ,@pEMPLOYEE_ID
    ,@pREQUEST_ID
    ,@pEmployeeRoleId
);
