



CREATE   PROCEDURE  [HRSC_APP].[usp_Update_Alternate_Contact_Manager]
	@pREQUEST_ID BIGINT, 
	@pEmployee_ID BIGINT,
	@pEmployeeRoleId BigInt, 
	@pUSER_UPDATED VARCHAR(250)

WITH EXEC AS CALLER
AS

